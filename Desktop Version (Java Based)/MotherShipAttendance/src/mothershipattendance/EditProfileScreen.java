        jScrollPane1.setEnabled(false);
        jTextArea1.setEnabled(false);
        
        if (tracker.getCurrentDesignation().equalsIgnoreCase("Student")) {
            
            FindProfilePictures find = new FindProfilePictures();
            Student s1 = new Student(0, 0, "", "", "", "", "", "", "", "", "", "", "", "", "");
            student = tracker.getCurrentStudentInformation();
            
            s1.setStudentEmail(jTextField4.getText());
            s1.setStudentPhone(jFormattedTextField1.getText());
            s1.setStudentAddress(jTextField6.getText());
            s1.setStudentBiography(jTextArea1.getText());
            
            String imageFinder = "" + jLabel2.getIcon().toString();
            
            String[] imageList = find.getImageIcons(imageFinder);
            
            s1.setStudentProfileImageLarge(imageList[0]);
            s1.setStudentProfileImageMedium(imageList[1]);
            s1.setStudentProfileImageSmall(imageList[2]);
            s1.setStudentProfileImageXS(imageList[3]);
            
            if (!s1.getStudentEmail().equalsIgnoreCase(student.getStudentEmail())) {
                if (s1.getStudentEmail().equalsIgnoreCase("Default")) {
                    student.setStudentEmail("");
                }
                else {
                    student.setStudentEmail(s1.getStudentEmail());
                }
            }
            
            if (!s1.getStudentPhone().equalsIgnoreCase(student.getStudentPhone())) {
                if (s1.getStudentPhone().equalsIgnoreCase("(   ) -     -     ") || s1.getStudentPhone().equalsIgnoreCase("Default")) {
                    student.setStudentPhone("");
                }
                else {
                    student.setStudentPhone(s1.getStudentPhone());
                }
            }
            
            if (!s1.getStudentAddress().equalsIgnoreCase(student.getStudentAddress())) {
                if (s1.getStudentAddress().equalsIgnoreCase("Default")) {
                    student.setStudentAddress("");
                }
                else {
                    student.setStudentAddress(s1.getStudentAddress());
                }
            }
            
            if (!s1.getStudentBiography().equalsIgnoreCase(student.getStudentBiography())) {
                if (s1.getStudentBiography().equalsIgnoreCase("Default") || s1.getStudentBiography().equalsIgnoreCase("Enter Your Biography Here.")) {
                    student.setStudentBiography("");
                }
                else {
                    student.setStudentBiography(s1.getStudentBiography());
                }
            }
            
            if (!s1.getStudentProfileImageLarge().equalsIgnoreCase(student.getStudentProfileImageLarge())) {
                student.setStudentProfileImageLarge(s1.getStudentProfileImageLarge());
                student.setStudentProfileImageMedium(s1.getStudentProfileImageMedium());
                student.setStudentProfileImageSmall(s1.getStudentProfileImageSmall());
                student.setStudentProfileImageXS(s1.getStudentProfileImageXS());
            }
            
            tracker.setCurrentStudentInformation(student);
            database.updateStudentInformation(student);
            
            MainScreenStudent main = tracker.getMainScreenStudentInstance();
            CreateImageFromText cift = new CreateImageFromText();
            
            int size = cift.determineFontSizeLucidaGrande(student.getStudentEmail(), 255, 25, 20);
            
            main.jLabel34.setFont(new java.awt.Font("Lucida Grande", 1, size));
            
            if (!student.getStudentEmail().equalsIgnoreCase("")) {
                main.jLabel34.setText(student.getStudentEmail());
            }
            
            main.jLabel12.setIcon(new javax.swing.ImageIcon(getClass().getResource(student.getStudentProfileImageLarge())));
            
            main.jLabel36.setText(student.getStudentPhone());
            
            String formattedHtml = "<html><left>" + student.getStudentAddress() + "</left></html>";
            main.jLabel38.setText(formattedHtml);
            
            main.jTextArea2.setText(student.getStudentBiography());
        }
        else if (tracker.getCurrentDesignation().equalsIgnoreCase("Instructor")) {
            
            FindProfilePictures find = new FindProfilePictures();
            Instructor i1 = new Instructor(0, "", "", "", "", "", "", "", "", "", "", "", "", "");
            instructor = tracker.getCurrentInstructorInformation();
            
            i1.setInstructorEmail(jTextField4.getText());
            i1.setInstructorPhone(jFormattedTextField1.getText());
            i1.setInstructorAddress(jTextField6.getText());
            i1.setInstructorBiography(jTextArea1.getText());
            
            String imageFinder = "" + jLabel2.getIcon().toString();
            
            String[] imageList = find.getImageIcons(imageFinder);
            
            i1.setInstructorProfileImageLarge(imageList[0]);
            i1.setInstructorProfileImageMedium(imageList[1]);
            i1.setInstructorProfileImageSmall(imageList[2]);
            i1.setInstructorProfileImageXS(imageList[3]);
            
            if (!i1.getInstructorEmail().equalsIgnoreCase(instructor.getInstructorEmail())) {
                if (i1.getInstructorEmail().equalsIgnoreCase("Default")) {
                    instructor.setInstructorEmail("");
                }
                else {
                    instructor.setInstructorEmail(i1.getInstructorEmail());
                }
            }
            
            if (!i1.getInstructorPhone().equalsIgnoreCase(instructor.getInstructorPhone())) {
                if (i1.getInstructorPhone().equalsIgnoreCase("(   ) -     -     ") || i1.getInstructorPhone().equalsIgnoreCase("Default")) {
                    instructor.setInstructorPhone("");
                }
                else {
                    instructor.setInstructorPhone(i1.getInstructorPhone());
                }
            }
            
            if (!i1.getInstructorAddress().equalsIgnoreCase(instructor.getInstructorAddress())) {
                if (i1.getInstructorAddress().equalsIgnoreCase("Default")) {
                    instructor.setInstructorEmail("");
                }
                else {
                    instructor.setInstructorAddress(i1.getInstructorAddress());
                }
            }
            
            if (!i1.getInstructorBiography().equalsIgnoreCase(instructor.getInstructorBiography())) {
                if (i1.getInstructorBiography().equalsIgnoreCase("Default") || i1.getInstructorBiography().equalsIgnoreCase("Enter Your Biography Here.")) {
                    instructor.setInstructorBiography("");
                }
                else {
                    instructor.setInstructorBiography(i1.getInstructorBiography());
                }
            }
            
            if (!i1.getInstructorProfileImageLarge().equalsIgnoreCase(instructor.getInstructorProfileImageLarge())) {
                instructor.setInstructorProfileImageLarge(i1.getInstructorProfileImageLarge());
                instructor.setInstructorProfileImageMedium(i1.getInstructorProfileImageMedium());
                instructor.setInstructorProfileImageSmall(i1.getInstructorProfileImageSmall());
                instructor.setInstructorProfileImageXS(i1.getInstructorProfileImageXS());
            }
            
            tracker.setCurrentInstructorInformation(instructor);
            database.updateInstructorInformation(instructor);
            
            MainScreen main = tracker.getMainScreenInstance();
            CreateImageFromText cift = new CreateImageFromText();
            
            int size = cift.determineFontSizeLucidaGrande(instructor.getInstructorEmail(), 255, 25, 20);
            
            main.jLabel7.setIcon(new javax.swing.ImageIcon(getClass().getResource(instructor.getInstructorProfileImageLarge())));
            
            main.jLabel34.setFont(new java.awt.Font("Lucida Grande", 1, size));
            
            if (!instructor.getInstructorEmail().equalsIgnoreCase("")) {
                main.jLabel34.setText(instructor.getInstructorEmail());
            }
            
            main.jLabel36.setText(instructor.getInstructorPhone());
            
            String formattedHtml = "<html><left>" + instructor.getInstructorAddress() + "</left></html>";
            main.jLabel38.setText(formattedHtml);
            
            main.jTextArea2.setText(instructor.getInstructorBiography());
            
        }
