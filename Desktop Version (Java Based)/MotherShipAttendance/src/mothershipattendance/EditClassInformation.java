    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedIndex = jComboBox6.getSelectedIndex();
        
        if (selectedIndex == 0) {
            
            WarningMessageManagementCreateNewClass wmmcnc = new WarningMessageManagementCreateNewClass("EditClassInformation");
            String htmlConvertedText = "";

            htmlConvertedText = "<html><center>Please Select A Valid Option Before Attempting to Continue.</center></html>";
            wmmcnc.jLabel6.setText(htmlConvertedText);
            
            wmmcnc.setVisible(true);
        }
        else {
            
            selectedClassType = jComboBox6.getItemAt(selectedIndex);
            System.out.println("Selected ClassType ID Number: " + selectedClassType.getClassID());
            
            jTextField2.setText(selectedClassType.getClassTitle());
            jTextField2.setForeground(new java.awt.Color(0, 0, 0));
            
            jTextField4.setText("" + selectedClassType.getClassNumber());
            jTextField4.setForeground(new java.awt.Color(0, 0, 0));
            
            jTextField6.setText("" + selectedClassType.getClassYear());
            jTextField6.setForeground(new java.awt.Color(0, 0, 0));
            
            jTextField3.setText("" + selectedClassType.getClassParticipants());
            jTextField3.setForeground(new java.awt.Color(0, 0, 0));
            
            if (selectedClassType.getClassDescription().equalsIgnoreCase("")) {
                jTextArea1.setText("Please Enter Additional Class Information Here. (Optional)");
                jTextArea1.setForeground(new java.awt.Color(204, 204, 204));
            }
            else {
                jTextArea1.setText(selectedClassType.getClassDescription());
                jTextArea1.setForeground(new java.awt.Color(0, 0, 0));
            }
            
            jCheckBox1.setSelected(true);
            
            // Select the Corresponding Field Associated with the Class in the ComboBox.
            for (int i = 0; i < jComboBox1.getItemCount(); i++) {
                
                String field = jComboBox1.getItemAt(i);
                
                String[] modifiedField = field.split(" ");
                field = modifiedField[1];

                modifiedField = field.split("\t");
                field = modifiedField[0];

                field = field.trim();
                
                if (selectedClassType.getClassField().equalsIgnoreCase(field)) {
                    jComboBox1.setSelectedIndex(i);
                    break;
                }
            }
            
            // Select the Corresponding Type Associated with the Class in the ComboBox.
            for (int i = 0; i < jComboBox2.getItemCount(); i++) {
                
                String type = jComboBox2.getItemAt(i).trim();
                
                if (selectedClassType.getClassStructureType().equalsIgnoreCase(type)) {
                    jComboBox2.setSelectedIndex(i);
                    break;
                }
            }
            
            // Select the Corresponding Section Associated with the Class in the ComboBox.
            for (int i = 0; i < jComboBox4.getItemCount(); i++) {
                
                String section = jComboBox4.getItemAt(i).trim();
                
                if (selectedClassType.getClassSection().equalsIgnoreCase(section)) {
                    jComboBox4.setSelectedIndex(i);
                    break;
                }
            }
            
            // Select the Corresponding Semester Associated with the Class in the ComboBox.
            for (int i = 0; i < jComboBox3.getItemCount(); i++) {
                
                String semester = jComboBox3.getItemAt(i).trim();
                
                if (selectedClassType.getClassSemester().equalsIgnoreCase(semester)) {
                    jComboBox3.setSelectedIndex(i);
                    break;
                }
            }
            
            // Select the Corresponding Section Associated with the Class in the ComboBox.
            for (int i = 0; i < jComboBox5.getItemCount(); i++) {
                
                String credits = jComboBox5.getItemAt(i).trim();
                
                if (selectedClassType.getClassCreditHours().equalsIgnoreCase(credits)) {
                    jComboBox5.setSelectedIndex(i);
                    break;
                }
            }
            
            showContents(true);
            showSearchBar(false);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

