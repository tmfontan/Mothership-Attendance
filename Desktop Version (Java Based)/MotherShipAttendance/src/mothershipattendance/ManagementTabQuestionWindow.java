    /*
     *  LoadingScreenController is a SwingWorker Class whose
     *  doInBackground() Method is invoked upon calling the execute()
     *  method in the main class. A SwingWorker Class is the only
     *  way to safely create a seperate thread that interacts with
     *  the main EventQueue class. Should you try to invoke change to the GUI
     *  object from a non EventQueue related class, it can cause
     *  unforseen nonrepeatable errors in the programs execution.
     *
     */
    public class ChoiceController extends SwingWorker<Void,Void> {
        // Method for Seperate EventQueue Thread
        // to Execute.
        @Override
        protected Void doInBackground() throws Exception {
            
            System.out.println("Getting Here 1-1");
            
            while (true) {
                
                if (tracker.getManagementScreenChoice() == 1) {
                    
                    System.out.println("Getting Here 5-1");
                    
                    boolean result = manager.removeInstructorClassLinkAndConnections(classIdentificationNumber, instructorIdentificationNumber);
                    
                    System.out.println("Getting Here 3-1");
                    
                    if (result == true) {
                        
                        MainScreen main = serverTracker.getMainScreenInstance();
                        
                        main.editClassInfo.setComponentsEnabled(false);
                        
                        ArrayList<Integer> classIDs = manager.getInstructorClassListIDs(tracker.getCurrentInstructorInformation().getInstructorID());
                        ArrayList<ClassType> classes = manager.getInstructorClassList(classIDs);
        
                        ClassType noClassesClassType = new ClassType(0, "NULL", 0, " -- No Associated Classes -- ", "", "", "", "Lecture", "", 0, 0);
                        ClassType defaultClassTypeUpdateClassInformationJPanel = new ClassType(0, "NULL", 0, " ---------------------------------------------------------------- ", "", "", "", "Lecture", "", 0, 0);

                        ArrayList<ClassType> recordsTabBoxListOne = new ArrayList<>();

                        for (int i = 0; i < classes.size(); i++) {
                            recordsTabBoxListOne.add(classes.get(i));
                        }

                        if (recordsTabBoxListOne.isEmpty()) {
                            recordsTabBoxListOne.add(0, noClassesClassType);
                        }
                        else {
                            recordsTabBoxListOne.add(0, defaultClassTypeUpdateClassInformationJPanel);
                        }
                        
                        DefaultComboBoxModel modelBoxOne = new DefaultComboBoxModel(recordsTabBoxListOne.toArray());
                        main.editClassInfo.jComboBox6.setModel(modelBoxOne);
                        main.editClassInfo.jComboBox6.setSelectedIndex(0);

                        main.editClassInfo.jLabel14.setEnabled(true);
                        main.editClassInfo.jComboBox1.setEnabled(true);
                        main.editClassInfo.showContents(false);
                        main.editClassInfo.showSearchBar(true);
                        
                        ClassSuccessfullyCreated csc = new ClassSuccessfullyCreated();
                        String htmlText = "<html><center>The Class has been Successfully Unlinked from your account.";
                        
                        csc.setTitle("Done");
                        csc.jLabel6.setText(htmlText);
                        csc.setVisible(true);
                        
                        dispose();
                    }
                    else {
                        ClassCreationFailed ccf = new ClassCreationFailed();
                        String htmlText = "<html><center>There was a problem unlinking the Class from your account. Please Try Again Later.";
                        
                        ccf.setTitle("Error Message");
                        ccf.jLabel6.setText(htmlText);
                        ccf.setVisible(true);
                        
                        MainScreen main = serverTracker.getMainScreenInstance();
                        main.editClassInfo.jLabel14.setEnabled(true);
                        main.editClassInfo.jComboBox1.setEnabled(true);
                        
                        dispose();
                    }
                    
                    break;
                }
                else if (tracker.getManagementScreenChoice() == 2)  {
                    MainScreen main = serverTracker.getMainScreenInstance();
                    main.editClassInfo.jCheckBox1.setSelected(true);
                    main.editClassInfo.setComponentsEnabled(true);
                        
                    break;
                }
            }
            
            System.out.println("Getting Here 2-1");
            tracker.setManagementScreenChoice(0);
            
            return null;
        }
        
        // This method is automatically invoked after the
        // doInBackground() method has finished
        // executing.
        @Override
        protected void done() {
            // Make the GuiFour Frame
            // invisible.
            //setVisible(false);
            //dispose();
        }
    }
