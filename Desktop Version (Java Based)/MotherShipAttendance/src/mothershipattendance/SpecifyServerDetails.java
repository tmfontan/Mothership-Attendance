    private void jPasswordField3jPasswordField3FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordField3jPasswordField3FocusLost
        String password = new String(jPasswordField3.getPassword());
        
        // If Text in Text Field is empty Make TextField
        // Display Enter Username upon focus lost.
        if (password.equalsIgnoreCase("")) {
            // Display Text
            jPasswordField3.setForeground(new java.awt.Color(153, 153, 153));
            jPasswordField3.setEchoChar((char)0);
            jPasswordField3.setText("Password");
        }
        else if (password.equalsIgnoreCase("Password")) {
            jPasswordField3.setForeground(new java.awt.Color(153, 153, 153));
            jPasswordField3.setEchoChar((char)0);
        }
    }//GEN-LAST:event_jPasswordField3jPasswordField3FocusLost

