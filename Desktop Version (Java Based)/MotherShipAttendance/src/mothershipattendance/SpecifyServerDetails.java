    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        jLabel1.setVisible(false);
        jTextField1.setBackground(new java.awt.Color(255, 255, 255));
        jTextField2.setBackground(new java.awt.Color(255, 255, 255));
        jTextField3.setBackground(new java.awt.Color(255, 255, 255));
        jPasswordField3.setBackground(new java.awt.Color(255, 255, 255));
        
        String password = new String(jPasswordField3.getPassword());
        
        if (jTextField1.getText().equalsIgnoreCase("Display Name") || jTextField2.getText().equalsIgnoreCase("Host IP Address") || jTextField3.getText().equalsIgnoreCase("Port Number") || password.equalsIgnoreCase("Password")) {
            counter = 0;
            
            if (jTextField1.getText().equalsIgnoreCase("Display Name")) {
                counter++;
            }
            if (jTextField2.getText().equalsIgnoreCase("Host INET4 IP Address")) {
                counter++;
            }
            if (jTextField3.getText().equalsIgnoreCase("Port Number")) {
                counter++;
            }
            if (password.equalsIgnoreCase("Password")) {
                counter++;
            }
            
            if (counter > 1) {
                WarningMessageSSMultipleFields warn = new WarningMessageSSMultipleFields();
                warn.setVisible(true);
            }
            else if (counter == 1) {
                if (jTextField1.getText().equalsIgnoreCase("Display Name")) {
                    WarningMessageSSDisplayName warn = new WarningMessageSSDisplayName();
                    warn.setVisible(true);
                }
                else if (jTextField2.getText().equalsIgnoreCase("Host INET4 IP Address")) {
                    WarningMessageSSHostIPAddress warn = new WarningMessageSSHostIPAddress();
                    warn.setVisible(true);
                }
                else if (jTextField3.getText().equalsIgnoreCase("Port Number")) {
                    WarningMessageSSPortNumber warn = new WarningMessageSSPortNumber();
                    warn.setVisible(true);
                }
                else if (password.equalsIgnoreCase("Password")) {
                    WarningMessageSSPassword warn = new WarningMessageSSPassword();
                    warn.setVisible(true);
                }
            }
        }
        else {
            try {
                String tempString = jTextField3.getText();
                int tempNum = Integer.parseInt(tempString);

                if (tempNum < 5000 || tempNum > 9000) {
                    WarningMessageSSInvalidPortNumber port = new WarningMessageSSInvalidPortNumber();
                    port.setVisible(true);
                }
                else {
                    serverTracker.setDisplayName(jTextField1.getText());
                    serverTracker.setPassword(password);
                    serverTracker.setPortNumber(tempNum);
                    
                    Server server = new Server(tempNum);
                    serverTracker.setCurrentServerInstance(server);
                    serverInstanceStarted = true;
                    
                    AttendanceServerCreationSuccessful attendance = new AttendanceServerCreationSuccessful();
                    
                    ThreadStarter starter = new ThreadStarter();
                    String[] args = {"Server"};
                    starter.main(args);

                    attendance.setVisible(true);
                    dispose();
                }
            }
            catch (NullPointerException e) {
                WarningMessageSSInvalidPortNumber port = new WarningMessageSSInvalidPortNumber();
                port.setVisible(true);
            }
            catch (NumberFormatException nfe) {
                WarningMessageSSInvalidPortNumber port = new WarningMessageSSInvalidPortNumber();
                port.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

