    private void formWindowClosed(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosed
        TrackerObject tracker = new TrackerObject();
        
        if (tracker.getCurrentDesignation().equalsIgnoreCase("Student")) {
            AccountCreationStudent.jTextField2.setBackground(new java.awt.Color(255, 102, 102));
            AccountCreationStudent.jTextField2.setForeground(new java.awt.Color(0, 0, 0));

            AccountCreationStudent.jLabel2.setVisible(true);
            AccountCreationStudent.jLabel2.setText("Please Enter A Response For the 'Last Name' Field.");

            AccountCreationStudent.jTextField1.setEnabled(true);
            AccountCreationStudent.jTextField2.setEnabled(true);
            AccountCreationStudent.jTextField3.setEnabled(true);
            AccountCreationStudent.jTextField4.setEnabled(true);
            AccountCreationStudent.jTextField5.setEnabled(true);
            AccountCreationStudent.jTextField6.setEnabled(true);
            AccountCreationStudent.jPasswordField1.setEnabled(true);
            AccountCreationStudent.jPasswordField2.setEnabled(true);
            AccountCreationStudent.jButton1.setEnabled(true);

            // Take Auto Focus Away From JTextField1 So the User May
            // See The Displayed Error Message and Red Colored Box.
            AccountCreationStudent.jButton1.requestFocusInWindow();
        }
        else if (tracker.getCurrentDesignation().equalsIgnoreCase("Instructor")) {
            AccountCreationInstructor.jTextField2.setBackground(new java.awt.Color(255, 102, 102));
            AccountCreationInstructor.jTextField2.setForeground(new java.awt.Color(0, 0, 0));

            AccountCreationInstructor.jLabel2.setVisible(true);
            AccountCreationInstructor.jLabel2.setText("Please Enter A Response For the 'Last Name' Field.");

            AccountCreationInstructor.jTextField1.setEnabled(true);
            AccountCreationInstructor.jTextField2.setEnabled(true);
            AccountCreationInstructor.jTextField3.setEnabled(true);
            AccountCreationInstructor.jTextField4.setEnabled(true);
            AccountCreationInstructor.jTextField5.setEnabled(true);
            AccountCreationInstructor.jPasswordField1.setEnabled(true);
            AccountCreationInstructor.jPasswordField2.setEnabled(true);
            AccountCreationInstructor.jButton1.setEnabled(true);

            // Take Auto Focus Away From JTextField1 So the User May
            // See The Displayed Error Message and Red Colored Box.
            AccountCreationInstructor.jButton1.requestFocusInWindow();
        }
    }//GEN-LAST:event_formWindowClosed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        TrackerObject tracker = new TrackerObject();
        
        if (tracker.getCurrentDesignation().equalsIgnoreCase("Student")) {
            AccountCreationStudent.jLabel3.setVisible(false);
        }
        else if (tracker.getCurrentDesignation().equalsIgnoreCase("Instructor")) {
            AccountCreationInstructor.jLabel3.setVisible(false);
        }
    }//GEN-LAST:event_formWindowOpened

